// Convert JSON gmaps style to URL version for the Static Maps API
// thanks to: https://stackoverflow.com/questions/19115223/converting-google-maps-styles-array-to-google-static-maps-styles-string#
function convertToStaticStyles(stylesArray) {
  var result = [];
  stylesArray.forEach(function(v, i, a){
    var style='';
    if (v.stylers.length > 0) { // Needs to have a style rule to be valid.
      style += (v.hasOwnProperty('featureType') ? 'feature:' + v.featureType : 'feature:all') + '|';
      style += (v.hasOwnProperty('elementType') ? 'element:' + v.elementType : 'element:all') + '|';
      v.stylers.forEach(function(val, i, a){
        var propertyname = Object.keys(val)[0];
        var propertyval = val[propertyname].toString().replace('#', '0x');
        style += propertyname + ':' + propertyval + '|';
      });
    }
    result.push('style='+encodeURIComponent(style));
  });

  return result.join('&');
}

